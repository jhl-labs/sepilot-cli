def get_dataset_config(dataset_name: str) -> dict | None:
    return PREDEFINED_DATASETS.get(dataset_name)


def get_preset_config(dataset_type: str, size: int) -> dict | None:
    """데이터셋 타입과 크기로 프리셋 config 반환.

    Args:
        dataset_type: "verified", "lite", "full" 중 하나
        size: 7, 30, 50 중 하나

    Returns:
        프리셋 config dict 또는 None (없으면)
    """
    key = f"{dataset_type}_{size}"
    return PREDEFINED_DATASETS.get(key)


def list_presets() -> list[dict]:
    """모든 프리셋 목록 반환.

    Returns:
        각 프리셋의 name, description, dataset_name, count를 담은 dict 목록
    """
    result = []
    for config in PREDEFINED_DATASETS.values():
        result.append({
            "name": config["name"],
            "description": config.get("description", ""),
            "dataset_name": config.get("dataset_name", "princeton-nlp/SWE-bench"),
            "count": len(config.get("instance_ids", [])),
        })
    return result
